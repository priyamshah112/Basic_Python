#9.	Write a program using functions to output the area of 
         # ( a )   circle (p*r2), where p=3.14
          #( b )  square (side * side )
          #( c )  rectangle (length * breadth )
def circle(r):
    from math import pi
    return (pi*r**2)
def square(side):
    return side*side
def rectangle(length,breadth):
    return (length*breadth)

